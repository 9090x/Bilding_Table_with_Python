from prettytable import PrettyTable as ПретиТэйбл

принт = print
инпут = input
рэнж = range

тэбл = ПретиТэйбл()
тэбл.field_names = ["№", "нэйм", "прайс", "валуе", "фул прайс"]

for и in рэнж(3):
    продукт = инпут("введите название продукта: ")
    прайс = инпут("укажите цену продукта: ")
    квалити = инпут("укажите количество продукта: ")
    тэбл.add_row([и + 1, продукт, прайс, квалити, прайс * квалити])

принт(тэбл)
